    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        // TODO add your handling code here:
        
        TableModel mm = (TableModel) tabela.getModel();
        ArrayList<Ugovor> ugovori = mm.getUgovori();
        Request request = new Request(Operations.SACUVAJ, ugovori);
        try {
            Response response = Komunikacija.getInstanca().nesto(request);
            if(response.getResponseType().equals(ResponseType.SUCCESS)){
                JOptionPane.showMessageDialog(this, "Uspesno sacuvano");
                isprazniTabelu();
            }else{
                 JOptionPane.showMessageDialog(this, "Neuspesno sacuvano");
                 return;
            }
        } catch (Exception ex) {
            Logger.getLogger(Glavna.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnSacuvajActionPerformed

   
