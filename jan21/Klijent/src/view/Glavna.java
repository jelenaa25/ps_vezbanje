    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        // TODO add your handling code here:
        Ugovor ug = new Ugovor();
        Paket paket = (Paket) cmbPaket.getSelectedItem();
        double cena = paket.getCena();
        if(jrDA.isSelected()){
        cena = cena+cena*0.1;
        ug.setCena(cena);
        }
        if(cmbVrstaUg.getSelectedItem().equals("Bez ugovora")){
            cena = cena+cena*0.1;
            ug.setCena(cena);
        }
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy.");
        try {
            ug.setDatumEvidentiranja(sdf.parse(txtDAtum.getText()));
            if(jrDA.isSelected()){ug.setDodatneUsl(1);}else{ug.setDodatneUsl(0);}
            ug.setPaket(paket);
            ug.setPretplatnik((Pretplatnik) cmbPretpl.getSelectedItem());
            ug.setVrsta((String) cmbVrstaUg.getSelectedItem());
            TableModel mm = (TableModel) tabela.getModel();
            mm.dodaj(ug);
        } catch (ParseException ex) {
            Logger.getLogger(Glavna.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDodajActionPerformed

